//Viết chương trình để tạo ra một danh sách (List)
// chứa các chuỗi từ một String cho sẵn và in ra các chuỗi có độ dài lớn hơn 3 ký tự

import java.util.ArrayList;
import java.util.List;

public class BT02 {
    public static void main(String[] args) {
        String inputString = "Chuỗi ví dụ để kiểm tra";
        List<String> words = new ArrayList<>();

        // Tách chuỗi thành các từ và thêm vào danh sách
        for (String word : inputString.split("\\s+ ")) {  // Tách theo khoảng trắng
            words.add(word);
        }

        // In các chuỗi có độ dài lớn hơn 3
        System.out.println("Các chuỗi có độ dài lớn hơn 3 : ");
        for (String word : words) {
            if (word.length() > 3) {
                System.out.println(word);
            }
        }

    }
}






























































































































































































































































































